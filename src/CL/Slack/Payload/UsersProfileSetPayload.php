namespace CL\Slack\Payload;

/**
 * @author Alexis MARQUIS <alexis@marquis.me>
 *
 * @link Official documentation at https://api.slack.com/methods/users.profile.set
 */
class UsersProfileSetPayload extends AbstractPayload
{
    private $profile;

    private $user;

    /**
     * @return mixed
     */
    public function getProfile()
    {
        return $this->profile;
    }

    /**
     * @param mixed $profile
     */
    public function setProfile($profile)
    {
        $this->profile = $profile;
    }

    /**
     * @return mixed
     */
    public function getUser()
    {
        return $this->user;
    }

    /**
     * @param mixed $user
     */
    public function setUser($user)
    {
        $this->user = $user;
    }




    /**
     * @inheritdoc
     */
    public function getMethod()
    {
        return 'users.profile.set';
    }
}
